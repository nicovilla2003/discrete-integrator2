package com.example.quoridorjavafx.model.javaComponents;

public class Node {
    private int x;  // X-coordinate
    private int y;  // Y-coordinate

    public Node(int x, int y) {
        this.x = x;
        this.y = y;

    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
